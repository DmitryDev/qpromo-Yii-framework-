                <h3>Submitting Vector Art</h3>
                <p>Two main types of graphic files exist on computers today: vector art and raster art. Nearly all orders require vector art for production.</p>

                <div class="imprint2_list">
                    <div class="imprint_item">
                        <div class="tit dinpro">Vector</div>
                        <p>Vector art is composed using mathematical formulas to draw curves and lines, making it fit for precision printing. 'Zooming in' on vector art will reveal no loss of curve/edge quality. because the graphic is not based on a finite number of squares on a grid.</p>
                        <p>File types include: .eps, .ai, .pdf, .cad, .swf, and others.</p>
                        <div class="item_thumb">
                            <img src="images/help_35.jpg" />
                        </div>
                    </div>
                    <div class="imprint_item">
                        <div class="tit dinpro">Raster Art</div>
                        <p>Raster art is composed by displaying many different color squares on a larger rectangular grid to create an image (think of the digital letters of a stock exchange ticker). 'Zooming in' on raster art reveals these squares immediately. Most popular image formats are raster art files. </p>
                        <p>File types include: .gif, .jpg, .png, .bmp, .psd, .psb, and others.</p>
                        <div class="item_thumb">
                            <img src="images/help_32.jpg" />
                        </div>
                    </div>
                </div>

                <div class="well">
                    <span class="dinpro">Important Note:</span> All of the raster art file types listed are always 100% raster art, but vector art file-types can be comprised of a mix of raster and vector art. Consult your design department or your Qpromo sales representative if you are not sure if your file is setup in vector format. If it is not, you may want to consider asking your design department to create a vector art file, though we are happy to do the vectorizing for you. Graphics submitted as raster art are subject to a vectorizing fee.
                </div>

                <h3>Identifying Your Pantone Colors</h3>
                <p>Before proceeding, you should know that process printing, laser engraving, and hot-stamping do not require a Pantone color to be identified. You can stop now if you are branding products with any of those options.</p>

                <h3>Color Consistency</h3>
                <p>Because of manufacturing differences and digital color profiles, colors can be tricky when viewed on computer monitors. For example, dark blue on one monitor might look like purple on another. Mockups alone -- whether created by you in our Logo Lab or by your Qpromo sales representative -- cannot be trusted for accurate color representation.</p>
                <p>To help with consistency in print mediums, most large organizations have specific colors identified for use in their logos. Many use the Pantone Color Matching System (PMS), a proprietary standard also used by most commercial printers.</p>

                <h3>Pantone Solid-Coated Colors Required For Color Imprinting</h3>
                <p>We use Pantone Solid-Coated inks for printing all color and multi-color orders. If you already have these identified then you're all set; just communicate them to your sales representative when submitting graphics. If you don't already have specific colors but wish to choose them yourself, most professional graphic programs have the Pantone color palettes built-in, so you (or your design department) can select them there. You can also view our online PMS guide to select a Pantone color.</p>
                <p>For best results, it is highly recommend that you use a Pantone Color Guide (ask your design department if they have a Pantone Color Guide handy) to choose the correct color. If you wish, we will choose Pantone Solid-Coated colors that match your given colors as closely as possible.</p>

                <div class="well">
                    <span class="dinpro">Important Note:</span> All printed colors must be set to 100% opacity and fill. Different tones/fills of the same color are printed as separate colors and charged accordingly.
                </div>

                <h3>Black And White Do Not Require Pantone Colors (Gray Still Does)</h3>
                <p>Black and white are the only colors which do not require Pantone matching. However, all shades of gray do require matching.</p>
